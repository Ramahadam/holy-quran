def main():
    script_dir = os.path.dirname(os.path.abspath(__file__))
    project_dir = os.path.dirname(script_dir)
    verses_path = os.path.join(project_dir, 'assets', 'quran', 'verses.json')
    checksums_path = os.path.join(project_dir, 'assets', 'quran', 'checksums.txt')

    print(f"PAGE_DATA entries: {len(PAGE_DATA)}")
    if len(PAGE_DATA) != 604:
        print(f"ERROR: Expected 604 page boundaries, got {len(PAGE_DATA)}")
        return

    with open(verses_path, 'r', encoding='utf-8') as f:
        verses = json.load(f)

    # Remove any existing page field
    for v in verses:
        v.pop('page', None)

    # Build page assignments using binary-search-style approach:
    # A verse belongs to page P if its (surah, verse) >= PAGE_DATA[P-1]
    # and < PAGE_DATA[P] (for the next page).
    for v in verses:
        s = v['surahNumber']
        vn = v['verseNumber']
        # Binary search for the page
        page = _find_page(s, vn)
        v['page'] = page

    # Verify
    pages_used = set(v['page'] for v in verses)
    print(f"Assigned pages to {len(verses)} verses")
    print(f"Pages used: {min(pages_used)}-{max(pages_used)} ({len(pages_used)} unique pages)")

    # Check for page 0 (unassigned)
    unassigned = [v for v in verses if v['page'] == 0]
    if unassigned:
        print(f"WARNING: {len(unassigned)} verses with page=0!")

    # Write updated verses.json
    output = json.dumps(verses, ensure_ascii=False, indent=2)
    with open(verses_path, 'w', encoding='utf-8') as f:
        f.write(output + '\n')

    # Update checksum
    new_checksum = hashlib.sha256(output.encode('utf-8')).hexdigest()

    with open(checksums_path, 'r', encoding='utf-8') as f:
        lines = f.readlines()

    new_lines = []
    for line in lines:
        if 'verses.json' in line:
            new_lines.append(f"{new_checksum}  verses.json\n")
        else:
            new_lines.append(line)

    with open(checksums_path, 'w', encoding='utf-8') as f:
        f.writelines(new_lines)

    print(f"Updated {verses_path}")
    print(f"New checksum: {new_checksum}")


def _find_page(surah: int, verse: int) -> int:
    """Binary search for which page a (surah, verse) falls on."""
    lo, hi = 0, len(PAGE_DATA) - 1
    result = 0
    while lo <= hi:
        mid = (lo + hi) // 2
        ps, pv = PAGE_DATA[mid]
        if (surah, verse) >= (ps, pv):
            result = mid
            lo = mid + 1
        else:
            hi = mid - 1
    return result + 1  # 1-indexed pages


if __name__ == '__main__':
    main()
